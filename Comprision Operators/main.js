// Comparision Operators
// < , > , <= , >= , == , === 
var b = 100;
console.log((b < 100)); // S is lower than 100
console.log((b > 100)); // S is greater than 100
console.log((b <= 100)); // S is lower than 100 or equal
console.log((b >= 100)); // S is greater than 100 Or equal
console.log((b == 100)); // S is equal than 100 
